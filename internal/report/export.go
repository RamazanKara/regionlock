// SARIF renders failing findings as SARIF 2.1.0 for GitHub code scanning.
func (r Report) SARIF() ([]byte, error) {
	driver := sarifDriver{
		Name:           "regionlock",
		InformationURI: "https://github.com/RamazanKara/regionlock",
		Version:        r.Version,
	}
	seenRule := map[string]bool{}
	var results []sarifResult
	for _, f := range r.Findings {
		if f.Status != rules.Fail {
			continue
		}
		if !seenRule[f.RuleID] {
			seenRule[f.RuleID] = true
			driver.Rules = append(driver.Rules, sarifRule{
				ID:               f.RuleID,
				Name:             f.RuleName,
				ShortDescription: sarifText{Text: f.RuleName},
				HelpURI:          "https://github.com/RamazanKara/regionlock#what-each-control-evidences",
				Properties: sarifRuleProps{
					SecuritySeverity: severityScore(f.Severity),
					Tags:             []string{"regionlock", "data-residency", "compliance"},
				},
			})
		}
		results = append(results, sarifResult{
			RuleID:  f.RuleID,
			Level:   sarifLevel(f.Severity),
			Message: sarifText{Text: fmt.Sprintf("%s [%s]", f.Message, articleList(f.Articles))},
			Locations: []sarifLocation{{
				PhysicalLocation: sarifPhysical{
					ArtifactLocation: sarifArtifact{URI: sourceURI(f.Source)},
				},
			}},
		})
	}
	doc := sarifDoc{
		Schema:  "https://json.schemastore.org/sarif-2.1.0.json",
		Version: "2.1.0",
		Runs:    []sarifRun{{Tool: sarifTool{Driver: driver}, Results: results}},
	}
	return jsonIndent(doc)
}

func severityScore(sev string) string {
	switch sev {
	case "high":
		return "8.0"
	case "medium":
		return "5.0"
	default:
		return "3.0"
	}
}

func sarifLevel(sev string) string {
	if sev == "high" {
		return "error"
	}
	return "warning"
}

func sourceURI(source string) string {
	if source == "" {
		return "cluster"
	}
	// Strip the "#<doc-index>" traceability suffix for a clean file URI.
	if i := strings.LastIndex(source, "#"); i >= 0 {
		return source[:i]
	}
	return source
}

type sarifDoc struct {
	Schema  string     `json:"$schema"`
	Version string     `json:"version"`
	Runs    []sarifRun `json:"runs"`
}
type sarifRun struct {
	Tool    sarifTool     `json:"tool"`
	Results []sarifResult `json:"results"`
}
type sarifTool struct {
	Driver sarifDriver `json:"driver"`
}
type sarifDriver struct {
	Name           string      `json:"name"`
	InformationURI string      `json:"informationUri"`
	Version        string      `json:"version"`
	Rules          []sarifRule `json:"rules"`
}
type sarifRule struct {
	ID               string         `json:"id"`
	Name             string         `json:"name"`
	ShortDescription sarifText      `json:"shortDescription"`
	HelpURI          string         `json:"helpUri"`
	Properties       sarifRuleProps `json:"properties"`
}
type sarifRuleProps struct {
	SecuritySeverity string   `json:"security-severity"`
	Tags             []string `json:"tags"`
}
type sarifResult struct {
	RuleID    string          `json:"ruleId"`
	Level     string          `json:"level"`
	Message   sarifText       `json:"message"`
	Locations []sarifLocation `json:"locations"`
}
type sarifLocation struct {
	PhysicalLocation sarifPhysical `json:"physicalLocation"`
}
type sarifPhysical struct {
	ArtifactLocation sarifArtifact `json:"artifactLocation"`
}
type sarifArtifact struct {
	URI string `json:"uri"`
}
type sarifText struct {
	Text string `json:"text"`
}
